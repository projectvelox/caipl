Biag ni Lam Ang 
(epiko ng mga Iloko)
Ang epikong ito ay isinulat ni Pedro Bukanag. Ang may Akdang ito ay sinasabing itinapon ng mga magulang sa ilog ng Abra nuong siyay sanggol pa dahilan ay bulag nang inianak. Subalit isang babae ang nakapulot sa kanya at siyay ibinigay sa isang Agustina. Pinangangalanan siya na Pedro Bukanag, pinalaki at pinag-aral hanggang siyay maging dalubhasa sa Kastila at sa samtay (wikang Iloko) sakalukuyan kinila siyang “Ama ng Panitikan ng Iloko”. Sa kaniya rin ang hango ang salitang “Bukanegan” na nangangahalugan sa Tagalog ng Balgtasan”.
Ang Banghay ng Kasyasayang Biag ni Lam Ang
Sa Nabuan (sakop ngayon ng La Union) ay may nakatirang mag asawa na nangangalang Don Juan at  Namongan. Nang malapit ng mganganak si Namongan si Don Juan ay napasa sa bundok upang parusahan ang isang pangkat ng mga Igorot. Hindi pa siya nakababalik ng magsilang ng sanggol si Namogan. Ang sanggol ay nagsalita agad na hininging  “Lam Ang” ang ipangalan sa kanya. Siya na rin pumili ng kaniyang magiging ninang. At dahil sa ang ina lamang niya ang kaniyang nakikitang nag –aalga sa kanya ay naitanong niya kung sino at nasaan ang kanyang ama.
Nang siyam na buwan na si Lam Ang  ang ama ay di pa nagbabalik ang batay sumunod sa kabundukan. Sa daan ay napanaginip niyang ipinagdirawang ng mga Igorot ang pagkamatay ng kanyang ama. Galit siyang nagising mabilis na nilakbay ang tirahan ng Igorot na inabot niyang nangasasayawang paligid-ligid sa pugot na ulo ng kanyang ama. Pinatay ni Lam Ang ang mga Igorot, maliban sa isang pinahirapan muna bago pinawalan.
Nang magbalik sa Nabuan, si Lam Ang ay pinaliguan ng ilang babaing kaibigan sa Ilog ng Amburaya. Sa kapal ng libag at sama ng amoy na nahugasan sa katawan, ang lahat ng  mga isda sa ilog ay nangamatay.
May naibigan si Lam Ang na isang babaing nangangalang Ines  Kanoyan sa bayan Kalamatian, karatig bayan ng Nabuan, Dumayo siya sa bayan nito upang manligaw, na kasama ang puting tandang at isang aso. Nakasalubong niya sa daan si Sumarang na manliligaw rin kay Ines. Nag-away sila at Madaling napatay si Sumarang. Sa harapan ng bahay nina Ines ay nakatagpo ni Lam Ang iba pang lumiligaw rito. Pinatilaok ni Lam Ang ang tandang at pagdaka’y isang bahay sa tabi bumagsak. Dumungaw si Ines. Pinatahol ni Lam Ang aso, at sa isang iglap, ang bahay ay natumba ay tumindig uli. Nanaog si Ines na kasama ang mga magulang. Ipinahayag ng tandang ang pag-ibig ni Lam Ang. Ang magulang ni Ines ay sumagot na payag sila kung mapapantayan ni Lam Ang ang kanilang kayamanan.
Umuwi si Lam Ang. Nang magbalik siya sa Kalanutan(Bayan ni Ines) ay sakay siya sa kaskong puno ng gintong ang halaga ay higit pa sa kayamanan nina Ines. Kaya’t sila’y ikinasal at nagkaroon ng malaking pagdiriwang.
Ilang panahon ang lumipas, pinagsabihan ng apo ng bayan si Lam Ang na turno nito ang manghuli ng raring.(Isda) Tungkulin ito ng lahat.
Ipinagtapat ni Lam Ang kay Ines na may kutob siyang siyay makagat at mapatay ng berbakan(isdang kauri ng pating)kapag nanghuli ng rarag. At nagkatotoo nga. Namatay si Lam Ang.
Sa laki ng lungkot ni Ines ay tinawag ng tandang ang isang maninisid at ipinatapon ang buto ni Lam Ang. Nang mapagbubu-buo ang kalansay sa pangangalaga ng tandang sa tahol ng aso, si Lam Ang ay muling nabuhay.
Ganap na naging maligaya ang mag-asawang Lam Ang  at Ines at namuhay ng payapa.

Alim
(epiko ng ipugaw)
	Noong unang panahon ang mga tao ay masagana, maligaya, at tahimik na pamumuhay. Ang daigdig ay pawang kapatagan ang  mamalas maliban sa dalawang bundok, ang bundok Amuyaw sa silangan at bundok kalawitan sa kanluran . Doon sila naninirahan sa pagitan ng dalawang bundok na ito. Walang mga suliranin ang mga tao tungkol sa kabuhayan. Ang mga pagkain ay nasapaligid nila. Mga biyas ng kawayan ang pinagsasaingan. Malaki rin ang butil ng kanilang bigas.
	Ang kanilang inumin ay nangangaling katas ng tubo na kung tawagin ay bayak. Kung nais nila ng ulam na isda ay sumasalok lamang nila sa sapa at ilog. Maamo ang usa at baboy ramo kaya madaling hulihin kung nais nilang kumain nito. Anupat kahit na ano ang maisipan nilang pagkain ay mayroon at sagana.
Subalit dumating sa kanilang buhay ang isang nakapalungkot na panyayari. Nagkaroon ng tagtuyot, hindi man lamang pumatak ang ulan. Namatay ang lahat ng mga halaman at mga hayop. Nangamatay rin ang ilang tao dahil sa uhaw at gutom. Naisip ng iba na hukayin ang tubig. Dahil sa lakas ng pagbalong ng tubig ay may mga namatay. Nagdiwang ang mga tao kahit may namatay, sapagkat sila’y may tubig na. Subalit hindi naghinto ang pagbalong ng tubig hanggang sa mgalunod ang lahat ng mga tao maliban sa magkapatid Wigan at Bugan. Ang lalaki si Wigan, ay napadpad sa bundok ng Amuyaw at ang babae si Bugan, ay ipinadpad ng baha sa bundok ng kaliwatan.
Sa wakas humupa rin ang baha Si Bugan ay nakapapaningas ng apoy at itoy nakita ni Wigan. Sumampa Si Wigan sa bundok Kalawitan at nagkita silang magkapatid. Naglakbay sila sa ibat ibang pook upang maghanap ng mga tao subalit wala silang natagpuang isa man. Nagtayo si Wigan ng isang kubo at dito niya iniwan si Bugan at siyay nagpatuloy nagpatuloy ng paghahanap. Sa wakas ay napagtanto niyang sila lamang magkapatid ang natira sa daigdig. Lumipas ang panahon at naramdaman ni Bugan na siya’y nagdadalang-tao. Dala ng kahihiyan sa sarili ay naisip niyang magpatiwakal subalit hinadlangan siya ng kanilang bathala sa makanungan.
Ikinasal silang magkapatid at sinabing ito’y hindi kasalanan sapagkat iyon ang lamang ang paraan upang dumaming muli ang mga tao sa daigdig.
Nagkaanak sila ng siyam-apat na babae at limang lalaki. Ikinasal ang apat na babae sa unang apat na lalaki kaya’t ang huling lalaki o bunso ay walang naging asawa. Ang ngalan ng bunsong ito ay Igon.
Sumapit ang panahong nagkaroon tagtuyot. Wala silang makain naisip nila na duulong kay Makanungan. Bilang Handog sa Bathala ay nanghuli si Wigan ng isang daga at inihandog sa Bathala. Nang hindi sila kawaan ng Bathala ay iginapos si Igon at pinatay upang na naman sa Bathalang Makanungan. Nilusan ni Makanungan  ang tagtuyot at dumalo pa sa kanilang handaan subalit labag sa kanyang kalooban ang ginawa kay Igon. Dahil daw sa ginawa nilang iyon kay Igon ay parurusahan sila, ang angkan ay magkakahiwalay, Ang magkakapatid na mag-asawa ay nagtungo sa hilaga, sa timog, sa silangan at sa kanluran. At sa pagkakataong sila’y magkatagpo-tagpo ay mag-away at magpapatayan sila. Ito raw sumpa ni Makanungan na tumalab sapagkat  kahit ang magkakapatid, mag ama, magpinsan o magkamag anak ay nagpapatayan.
