KALIGIRANG KASAYKAYAN
	Noon pa mang hindi pa dumating sa ating kapuluan ang mga Kastila, at maging ang iba pang mga dayuhan, ang ating mga ninuno ay may sarili nang panitikang nagtataglay ng kasaysayan ng ating lahi.
	Panitikan ang nagpapahiwatig ng tunay na pagkalahi natin.Nagsisilbi itong wika Pilipino na  nagbubunyag ng panlipunan at panlahing kaugalian ng ating pang-araw-araw na buhay, na nababakas sa mga kuwentong bayan, alamat, epiko, kantahing  bayan, kasabihan, bugtong,palaisipan ,sinaunang dula, at maikling kuwento.
	Mayroon na rin ang ating ninuno noon ng sariling baybayin o alpabetong kaiba sa kasalukuyang ginagamit na dinala ng mga kastila. Ito ay ang alibata-abakadang kawahig ng magi polinesyo ng unang ginagamit ng ating ninuno.
	Anumang talang pampanitikan ng atingmga ninuno sinulahat ng mga  naturang pari sa Paniniwalang ang mga iyon ay libro ng diyalblo.Maliban sa katuwirang yun nabuosa kanilang sirili na magiging sagabal ang mga talang nakuha nila sa madaling pagpalaganap ng pananampalatayang katoliko.
	Subalit mayroong mga talang di parin nasunog, Kabilang mga ito ay ang mga kantahing bayan na siya namang magpapatunay ng pagkaroon natin ng sariling kalingan.Mabils noon nagpapasalin-salin sa bibig ng mga katutubong mamayan hanggang dumating ang mga palimbagan na nagkaroon ng interes na ipalimbag ang mga napupulot na lolang pampanitikan ng matatandang Pilipino.
	Ang mga ninuno natin ay ginamit ng biyas ng kawayan, talukap ng bunga o niyog, dahon at balat ng punung kahoy  bilang sulatan. Ang mga panulat ay matutulis na kawayan o kahoy bilang sulatan. Ang mga panulat ay matutulis na kawayan o kahoy o dulo ng lanseta, at matutulis na buto at bakal. Kahit na ang bumbong na pansalok ng inumin ay tinatalaan nila ng mahalagang pangyayari sa buhay.
	Pinatunayan ng mga kastilang nanakop sa pilpinas na an gating mga ninuno ay talagang mahihilig sa mga tula, awit, kwento bugtong palaisipan  na magpahanggang ngayon ay nagdudulot pa sa atin ng kasiyahan at nagiging  tagapagbadya sa salinlahi ng tunay na kalinangan at kultura ng ating lahi.




MGA BAHAGI NG PANITIKANG PILPINO BAGO DUMATING ANG MGA KASTILA

	Bago pa dumating ang mga kastila,ipinanalagay na ang ating mga ninuno ay mayaman na sa alamat, kwentong bayan, epiko awiting bayan ng bugtong sawakain, palaisipan,at bulong. Mayroon na rin silang tula at dula noong panahon iyon.
	
ANG  ALAMAT
	Ang almat ay isang uri ng panitkan tuluyan na ang karaniwang paksa ay nagsasalaysay pinagmulan ng isang bagay,  pook, kalagayan, o katawagan
Mababakas na ang pangyayari ay likhang isip lamang, salat sa katotohanan at tunay na dina-kapananiwala subalit ang mga kaugaliang Filpino ay masasalin din sa mga akdang ito .Ang layunin nitoy manlibang, narito ang halimbawa.

       ANG MGA ALAMAT NG MGA TAGALOG 
	Di kaila Sa atin na tayung mga Pilipino ay binubuo ng nakaparaming lahi, may lahing Ilocano ,Kapampangan, Bisaya, Bikolano, at iba pa. Isa sa mga lahing nabanggit ang malalaman natin ang pinagmulan sa mga ito ay ang mga Tagalog.
	Sa isa raw na malawak na pook ng Luzon ay may isang nayong pasyalan ng mga binate. Ang nayong ito ay may maraming punongkahoy na nakatanim, mga bulaklak, parang at isang ilog na inaagusan ng malinaw na tubig. Hindi ang kagandahang ng nayon kundiang isang nakapagandang dalaga na wari’y nimpa ang nakaakit sa mga kabinataan kung bakas dinarayo nila ang nasabing pook.
Ang Dalagang ay si Maria. Malimit siyang magkaroon ng mga manliligaw na sadyang dumarayo at nakikipagsapalaran . Subalit si Maria ay walang sinagot ni isa sa mga binata. Pihikan ang kanyang puso. 
Palibhasa hindi siya masungit, kaya giliw sa kaniya ang mga binata. Walang tigil ang pagsamo sa kaniya ng mga ito, at sapagkat wala siyang napili ay umiisip siya ng paraan. Tinipon niya ang mga binatang yaon at nagwika siya ng ganito.
“Napakabuti ninyong lahat. Mahirap para sa akin ang mamili ng isa inyo. Kung gusto nyo ay daanin na lang sa pagsubok.”
“Kung siya mong nais ay wala kaming tutol.” Wika ng binata “Pumapayag kami.”
“Kung gayon ay pakinggan ang aking sasabihin. Sinuman sa inyo ang unang nakapagpadala ng isang malaking sawang buhay at malakas ay siya kong pakakakasalan.”bino ni Maria.
Natigilan ang mga biantang kaharap. Walang ano-anu ay narinig ang tinig ni Ilog na siyang Bumasag ng Katahimikan.
“Ako nangangako sa iyo Maria. Kahit buhay ko ang mgaing kapalit ay susundin ko ang iyong kahilingan.”
Si Ilog ay biantang kilala ng lahat sa katapangan. Umalis noon din ang binata upang tumupad sa utos ng nililiyag.
Nagkaroon ng  Bulung-bulungan ang binatang naiwan. Tinayak nilang hindi makakabalik si Ilog.
Naghintay sila subalit marami ng oras ang lumipas ay wala pa si Ilog. Maging si Maria ay nangamba na rin, sapagkat ikalulungkot din niya kung mapahamak ang isang matapang at masunurin ng dahil lamang sa kanyang biro.
Pagkaraan pa ng maraming  oras ay biglang dumating si Ilog. Gayon na lamang ang pagkagulo ng mga tao sa panunuod sa kagila-gilalas na katapangan ni Ilog na noon lamang nila nasaksihan sa mahaba ring panahon. Hawak ni Ilog sa batok at sa buntot ang malaking sawa.
Kasalukuyang nagkakagulo ang mga tao nang nagdaraan naman ang dalawang kastila. At nagtawag ang kanilang pansin,hindi ng nakita nilang hawak-hawak ni Ilog na sawa kundi kagandahan ni Maria.
“Maria ,” tila bayaning  sambit ni Ilog.  “Dala dala ko na ang sawang hniling mo. Ano pa ang ibig mong gawin ko sa sawang ito upang mapaligaya kita?” pagtanong ni Ilog.
“Tagain mo, Ilog,” ang ni Maria.
Noong lamang namangha ang dalawang Kastila sa pagkagulo ng mga tao. 		Naitanong nila kung anong pook ang kinaroroonan, subalit wala sa kanila nakapansin, dahil abala ang mga napagtanungan sa pagtingin sa ahas at kay Ilog. Nang Makita ni Maria na gusto pang manlaban ang sawa ay napasigaw siya ng malakas.
“Taga, Ilog! Taga, Ilog!” ang kaniyang na ang ibig sabihin na tagain pang muli ni Ilog ang ahas.
Sa narinig ng dalawang kastila, akala nila ay yaon na ang kasagutan sa kanilang tanong.  Inulit-ulit ang katagang Tagaliog, Tagailog hanggang sa malaon ay naging Tagalog.

KUWENTONG BAYAN 
Mapalad tayo sapagkat ang ating bansa ay mayaman sa mga kuwentong bayan kahit noon paman. Ang mga kuwentong bayan  ay madalas nangyayari sa loob at labas ng ating lugar. Ito ay madalas napagsasalin-salin sa bibig ng mga tao, kaya tang katotohanan sa kuwento ay mahirap tukuyin. May mga manunulat tayo sakasalukuyan na kung hindi ang hango ang kuwento sa matandang kuwentong bayan ay likha lamang ng malikot na imahinasyon.
PANITIKANG FILIFINO KASYSAYAN AT PAG-UNLAD 
Ang mga kwentong bayan ay binubuo ng mga kwentong kasaysayan sa buhay. Pakikipagsapalaran,pag-iibigan katalabutan, at katatawanan na nakapupulutan ng maga-gandang aral sa buhay. Ang kwento nito sa atin ay totoong kapakinabang, sapagkat nakapagsimula ito upang mapahalagahan ang ating kapaligiran, masuri ang ating katauhan at pagbabago ng ating pananaw sa buhay.
Narito ang halimbawa:
“SI BULAN AT SI ADLAW”
Noong unang panahon daw ay mag-asawang nangangalang Bulan at Adlaw. Sa tamis ng kanilang pagsasama ay nagkaanak sila ng marami. Nagpatuloy ng pag-aanak si Bulan hanggang sa mapuna si Adlaw na maraming –marami na pala ang mga anak nila nagsisikip na sila sa kanilang bahay. 
Naisip ni Adlaw na kausapin si Bulan na pagpapatayin nila ang iba nilang anak upang lumiwanag sa kanilang tinitirahan.
Tumutol si Bulan sa mungkahi ni Adlaw ito ang naging dahilan ng kanilang madalas na pag-aaway. Halos araw-araw ay nag-aaway sila. Nang hindi matiis ni Bulan, ay nagpasiya siyang makipaghiwalay na lamang siya kay Adlaw. Lalong nagalit si Adlaw. Pumayag din siyang makipaghiwalay kay Bulan sa kundisyong isasama lahat ni Bulan ang kanilang mga anak na huwag nang pakiktang muli sa kanya.
	Kaya ngayon, makikitang si Adlaw o ang araw nag iisang sumisikat sa araw, at si Bulan o ang Buwan ay sa gabi na lamang lumilitaw na kasama ng kaniyang mga anak na mga bituin. At kapag nagkakatagpo sila sumisidhi raw ang galit ni Adlaw kay Bulan. Tinutugis niya ito na siya raw dahilan ng pagkakaroon natin paminsan-minsan ng laho o eklipse.
