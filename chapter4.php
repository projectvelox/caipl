Si Graciano Lopez Jaena
(1856-1896)
Isinilang noong Disyembre 18, 1856, at binawian ng bahay noong Enero 20,1896, ang isa sa pinakadakilang bayani at henyo ng Pilipinas,si Graciano Lopez Jaena. Siya ay ipinagmamalaking anak ng Jaro, Ilo ilo, na ang katalinuhang taglay ay hinangan ng mga Kastila at Europeo. Isa siyang kilalang manunulat at manalumpati” sa Pilipinas. Siya ay nakagawa ng may 100 pananalumpati na magpahanggang ngayon ay binabassa ng mga makabagong Pilipino na tinipon at inilimbag sa imprenta ni Remigio Garcia, dating may ari ng tindahan ng aklat, “Manila Filatica.”
Si Graciano Lopez Jaena ay umalis sa Pilipinas noong 1887 sa tulong ng kanyang mayamang tiyuhing si Don Claudio Lopez upang makatakas sa pagpaparusa sa kanya ng kanyang mga kaaway, at humantong sa Valencia, ang pinakasentro ng kilusang Repulblicano ng mga Kastila tulad nina Pi y Margall, Morayta, Moret, Castelar,
Salmeron, at iba pa. Simula sa Valencia ay lumipat siya sa Barcelona at itinatag niya ang kauna-unahang magasin, ang “La Solidaridad,” na hindi naglaon ay naging opisyal na bibig ng “ Asociacion Hispano Filipina” na binubuo ng mga kastila at Pilipino na siyang lumalakad sa pagbabago ng mga reporma sa Pilipinas. Dahil dito, si Graciano Lopez Jaena ay nagtagumpay na ipamukha sa mga Kastila at sa mga tao sa mundo at ipakilala ang nagagawa ng isang mamahayag sa kanyang bayan sa pagpapasok ng pagbabago sa mga batas,reporma, tungo sa mabuting kabuhayan, pag-unlad ng kabihasnan at sariling kapakanan.
Si Lopez Jaena bagama’t  hindi naging propesor ay isa ring guro sa pamamagitan ng kanyang mga kaibigan at kamag-anakan sa Pilipinas.
Tulad nina Antonio Maria Regidor, Tomas G. del Rosario, at Felipe Calderon, siya ay tumindig sa paghiwalay ng simbahan sa pamahalaan, para sa walang bayad na pag-aaral, para sa mabuting pama-malakad ng edukasyon na pinamumuan ng pamahalaang may kalayaan sa pananampalataya, at para sa pagtatatag ng isang nagsasarili at malayang pamantasan.
Dahil sa pagkakahidwaan nina Dr. Jose Rizal at Marcelo H. del Pilar ukol sa kung sino ang namumuno sa asociacion Hispano Pilipina sa Madrid, si Graciano Lopez Jaena ay pumanig kay Rizal. Naisipan ni Lopez Jaena ang magbalik na rin sa Pilipinas upang maghingi ng abuloy para sa pagpapatuloy ng bago niyang pamahalaan na may pangalang “El Latigo Nacional” kay Marcelo H. del Pilar na noon ay abogado na at maraming dalang kuwarta sa simula ng pagtigil sa Espanya.
Si Graciano Lopez Jaena ay nagkasakit ng tuberculosis at namatay sa ospital na walang bayad sa Barcelona noong ika-20 ng Enero, 1896, labing isang buwan bago binaril ang malapit niyang kaibigan na si Dr. Jose Rizal, sa Luneta noong ika-30 ng Disyembre, 1896.

