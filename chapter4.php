KABANATA IV
KALIGIRANG KASAYSAYAN
Nagising pagkatapos nang higit  sa tatlong daang taong Pagkakahimlay ang mga natutulog na damdamin ng mga PilipinoNang isangkot  sa digmaan sa kabite ang tatlong paring sina Gomez,Burgos, at Zamora at patayin sa pamamagitan ng garote nang walang matibay na katibayan ng pagkakasala. Ito’y naganap noong Ika- 17 ng Pebrero, 1872. Naragdagan pa ito nang makapasok ditto ang diwang liberalismo sa pamamagitan ng pagkakabukas ng Pilipinas sa pandaigdig na kalakalan, at ang pagkakadala sa kapuluan ng liberal na lider sa tulad ni Gob. Carlos Maria de la Torre.

Naging lalong mahigpit ang pagbabanta ng mga kastila, subalitdi na nila nagawang pigilan pa ang nabuong mapanlabang damdamin ng mga Pilipino. Ang dating diwang maka-relihiyon ay naging makabayan at humihingi ng pagbabago sa pamamalakad ng simbahan at pamahalaan.
