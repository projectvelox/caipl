Dr.Jose Rizal
Jose Protacio Rizal Mercado Alonzo y Realonda ang buo niyang pangalan. Siya ay ipinanganak noong Hunyo 19,1861 sa bayan ng Kalamba, lalawigan ng Laguna. Naging unang guro niya ang kanyang ina na si Teodora Alonzo.

Nag-aral siya sa Ateneo de Manila. Nagsimula siyang mag-aral ng medisina sa Pamantasan ng Santo Tomas, nguni’t nagtapos sa Unibersidad Sentral ng Madrid. Nag-aral din siya sa pamantasan ng Berlin, Leipzig, at Heidelberg. Ang kanyang bahay ay nagwakas noong Disyembre 30, 1896, nang ipabaril siya ng mga Kastila na nagparatang sa kanya ng sedisyon at paghihimagsik laban sa pamahalaang Kastila. Ginamit niya ang sagisag na Laong-laan at Dimasalang sa kaniyang mga panulat.

