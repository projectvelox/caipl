Ang Tunay na Sampung Utos

Una –Ibig mo ang Diyos at ang iyong karangalan nang higit sa lahat ng bagay: Ang Diyos ay batis ng lahat ng katotohanan, ng lahat ng katarungan, at ang lahat ng gawain; at ang karangalan mo’y siyang tanging kapangyarihang mag-uutos sa iyo na ikaw ay maging matapat, mabait, at masipag.
Ikalawa –Sambahin mo ang Diyos sa paraang minamabuti at minamarapat ng iyong budhi: sapagkat sa iyong budhi, na humahatol sa masama mong gawa at pumupuri sa mabuti ay nakakausap mo ang iyong Diyos.
Ikatlo –Linangin mo ang mga sadyang katangiang kaloob ng Diyos,sa paggawa at pag-aaral ayon sa iyong kakayahan, na di lumalayo sa landas ng kagalingan at katarungan upang matamo ang sariling kadalisayang ikatutupad mo sa tungkuling ipinataw sa iyong Diyos sa buhay na ito, at sa ganito’y ikaw ay parangalan, at sa dangal mong ito’y luluwalhati ang Diyos mo.
Ikaapat –Ibigin mo ang iyong baying sunod sa Diyos at sa iyong karangalan at mahigit sa iyong sarili: pagkat ang bayan mo’y siyang tanging Paraisong kaloob ng Diyos sa iyo sa buhay na ito, ang tanging lupang tinubuan ng iyong lahi, ang tanging pamana ng iyong mga ninuno, at ang tanging pag-asa ng iyong kinabukasan; dahil sa bayan mo, ikaw ay may buhay, pag-ibig, kapakanan, ligaya, karangalan,at Diyos.
Ikalima –Pgsumikapan mo ang kaligayahan ng iyong bayan una kaysa sarili mo, na gagawin mo siyang maging kaharian ng katwiran, ng katarungan, at ng paggawa, pagkat kung ang bayan mo’y maligaya, ikaw at sampo ng iyong pamilya ay magiging maligaya rin.


Ikaanim –Pagsumikapan mo ang kasarinlan ng iyong bayan: pagkat ikaw lamang ang maaaring magkaroon ng tunay na adhika sa kaniyang ikauunlad at ikakatayog, sapagkat ang kaniyang kasarinlan ay siyang magbibigay ng iyong kalayaan; ang kaniyang kaunlaran ay siyang panggagalingan ng iyong kagalingan; at ang kaniyang katayuan ay siyang pagkukunan ng sarili mong luwalhati at pagkawalang kamatayan.

Ikapito –Huwag mong kilalanin sa iyong bayan ang kapangyarihan ninumang hindi inihalal mo at ng iyong mga kababayan: sapagkat ang kapangyarihan ay galing sa Diyos, at dahil sa ang Diyos ay nagsasalita sa pamamagitan ng budhi ng bawat tao, ang sino mang hinirang at inihayag ng budhi ng kabuuan ng mga tao ay siyang tanging maaaring gumamit ng tunay na kapangyarihan.

Ikawalo –Pagsumikapan mong makapagtatag ng isang Republika at kailanma’y hindi ng isang kaharian para sa iyong bayan: sapagkat ang huli’y nagpapatayog sa isa o iilang pamilya lamang at may paghaharing Manahan, ang una’y nakapagpaparangal at nagpapaging marapat sa isang lahi sa pamamagitan ng katwiran, nakapagpapadakila sa pamamagitan ng kalayaan, at nakapagpapasagana at ningning sa pamamagitan ng paggawa.

Ikasiyam –Mahalin mo ang kapwa tao gaya ng pagmamahal sa sarili mo: sapagkat ikinatang ng Diyos sa kaniya at sa iyo rin ang tungkuling ikaw ay tulungan at huwag gawin sa iyo ang hindi niya nais na gawin mo sa kaniya; ngunit kung ang kapwa mo, na di nakatupad sa ganitong tungkulin, ay maghangad ng laban sa iyong buhay, kalayaan, sa gayo’y sa ilalim ng batas ng pagtatanggol sa sarili ay igugupo mo siya’t lilipulin.

Ikasampo –Itatangi mo ang iyong kababayan higit sa kapwa mo; ipalalagay mo siyang kaibigan, kapatid, o kasamang kaugnay mo sa iisang kapalaran, kasukob sa ligaya at lungkot at sa magkakatulad na hangarin at kapakanan.

