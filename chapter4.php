ANG PANAHON NG TAHASANG PAGHIHIMAGSIK
KALIGIRANG KASAYSAYAN

Hindi naipagkaloob sa mga Pilipino ang mga hinihinging pagbabago ng mga propagandista. Naging bingi ang pamahalaan, nagpatuloy ang pang-aapi at pagsasamantala, at naging mahigpit pa sa mga Pilipino ang pamahalaan at simbahan. Ang mga mabuting balakin sana ng Inang Espanya  sa Pilipinas ay nasasalungat pa rin ng mga prayleng naghahari rito.

Nang dahil sa pangyayaring iyon, ilan sa mga mamamayang Pilipinong kabilang sa pangkat ng “La Liga Filipina” (isang samahang sibiko na pinaghihinalaang mapaghimagsik at naging dahilan ng pagkakatapon sa Dapitan ng nagtatag na si Jose Rizal) na tulad ni Andres Bonifacio, Emilio Jacinto, Apolinario Mabini, Jose Palma, Pio Valenzuela, at iba pa, ay nagsipagsabi na “wala nang natitirang lunas kundi ang  paghimagsik.” Ang naging laman ng panitikan ay pawang pagtuligsa sa pamahalaan at simbahan at pagbibigay-payo sa mga Pilipino upang magkaisa at maghanda nang matamo ang inaasahan na kalayaan.

