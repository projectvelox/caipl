Sino si Botod?
May dalawang tao ang nag-uusap at kanilang ang kanilang pinag-uusapan ay tungkol kay Pari Botod. Nasa plaza sila at nakita nila ito na may kasamang babae. Sinasampal ang babae at napaluhod  ito at nagmamakaawa na wari ay humihingi ng kapatawaran. Kasumpa-sumpang pari-diyata’t magagawa niya ito- Sanay kami sa gayong eksena. Sabin g kausap. Siya ang kura paroko sa aming bayan. Ang mga prayle pala ang may-ari ng mga paroko ditto? – Di kapani-paniwala. Talagang naririto sila at nagmamalabis hindi lamang sa ispiritwal na bagay kundi sa (politiko) pamahalaan at kalaswaan. Dapat ay lasunin ang ganyang uri ng tao. Darating ang araw t pagbabayaran din nila ang kanilang pagkakautang. Daig pa pala sa Tsina.
Paglalarawan:
Ang Pari Botod ay di niya pangalan o apelyido. Ang kahulugan ng Botod ay malaking tiyan at ito ang tinataguri sa kanya ng tao. Ang ngalang binyagan niya ay Ana dahil sa pinanganak siya sa kapistahan ni Santa Ana – ina ang Mahal na Birhen. Nais pa niyang tawaging Fray Botod kaysa Pari Ana. Siya ay taga-Aragon at ang mga magulang niya ay di niya nakilala.Siya ay natagpuan ng isang mangingisda sa ilog ng Ebro malapit sa simbahan ng “Our Lady of Pillar” – nang sumapit sa ika-14 ay tumakas at nagpunta sa Villadolid sa kumbinto ng Agustino. Siya 21 nang maatasang magtungo sa Pilipinas. At isa pa rin sa mga ugali niya ay pagiging magaslaw. Siya ay nag-anyong mahiyain ngunit pagkaraan ng limang taong pagkain ng  saging, papaya, at pagkatapos na maging paroko sa bayan nila ay naging mapagmalaki na at napakayaman. Malaking tao na siya ngayon, di kapani-paniwala. Pandak, bilugang mukha na parang buwan, bilugang pisngi. Makapal ang labi, maliliit na mga mata,mapulang ilong na malaki ang butas kay-daling makaamoy.Mamula-mulang buhok, bilugan ang ulo tulad ng bao ng niyog. Kunot ang noo at matalas tumingin. Napakalaking tiyang nakausli. Maikli ang leeg – iyan si Padre Botod. Ang buong katauhan ay kuha kay Don Quijote at katawa-tawang si Lancho Lanza.

