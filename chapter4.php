IBA PANG MGA PROPAGANDISTA
ANTONIO LUNA
Si Antonio Luna ay isang paramasyotikong dinakip at pinatapon ng mga Kastila sa Espanya. Sumanib siya sa Kilusang Propaganda at na-ambag ng kaniyang mga akda ay halos natutungkol sa kaugaliang Pilipino. At ang iba’y tumutuligsa sa pamamalakad ng mga Kastila. Ang ginamit niyang sagisag sa panulat ay Taga-ilog. Namatay siya sa gulang na 33 noong ika-7 ng Hunyo, 1899. Pinatay siya diumano ng mga tauhan ni Aguinaldo, sanhi ng mabilis niyang kabantugan na naging kaagaw niya sa pagtingin ng bayan.
Ang ilan sa kaniyang mga inakda ay ang mga sumusunod:
Noche Buena –naglalarawan ng tunay na buhay Filipino.
Se Divierten –(Naglilibang Sila) –isang pagpuna sa sayaw ng mga Kastila na halos di-maraanang sinulid ang pagitan ng mga nagsisipagsayaw.
La Tertulia Filipina –(Sa Piging ng mga Pilipino) –naglalahad ng isang kaugaliang Filipino na ipinalalagay niyang lalong mabuti kaysa kaugaliang Kastila.
Por Madrid –tumutuligsa sa mga Kastilang nagsasabing ang Pilipinas ay lalawigan ng Espanya ngunit ipinalalagay ng banyaga kapag siningilan ng selyo.
La Casa de Huespedes –(Ang Pangaserahan) paglalarawan ng isang pangaserahan na ang kasera’y naghahanap ng mangangasera hindi upang kumita, kundi upang maihanap ng mapapangasawa ang kaniyang anak.
Impresiones –ito’y isang paglalarawan sa ibayong kahirapang dinaranas ng isang mag-aaral na naulila sa amang kawal.

Ang sumusunod ay isang halaw sa akdang ito ni Taga-ilog.
