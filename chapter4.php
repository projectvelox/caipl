EMILIO JACINTO
(1875-1899)

Sagisag ng kabataang manghimagsik. Si Jacinto ay kanilang utak ng Katipunan, sapagkat tumayo siyang kanang-kamay ni Bonifacio, na hindi niya hiniwalayan liban na lamang sa huling yugtong paghihimagsik, nang mapasugo siya sa Laguna at si Bonifacio nama’y sa Cavite. Anak mahirap din, si Jacinto ay nagkaroon ng matiising magulang na nagsikap na mapapasok sa kolehiyo ang anak. Sa ganito, si Jacinto ay nakapag-aral at naging bihasa sa Kastila, na una niyang natutunan sa lansangang pinaglumagian niya ng bata pa. Lalong bihasa sa Kastila, nagpakahusay si Jacinto sa Tagalog, sapagkat ito ang wikang kailangan para makasapi sa Katipunan, hanggang makasulat ng mga maaalab na katha bilang pagsunod sa tanging layuning makaakit ng mga kaanib sa samahang mapanghimagsik. Matapat na makabayan, si Jacinto ay sumulat ng mga paksang makabayan at mapanghimagsik sa Tagalog at Kastila. Sa mga sinulat niya sa Tagalog, na nilagdaang Dimas Ilaw, ibinibilang ang tinanggap na Kartilya at Katipunan na pinagtibay gamitin sa halip ng Kartilya na inihanda ni Bonifacio, saka ang Pahayag, Sa mga kababayan, Ang kasalanan ni Cain, Pagkakatatag ng Pamahalaan sa Hukuman ng Silangan, at Samahan ng Bayan sa pangangalakal. Isa sa lalong malaking nagawa niya para sa kapakanan ng Katipunan, at sa makatuwid ssa rebolusyon, ay ang pagkakatatag at pagkakamahala niya ng kalayaan, ang tagapamansag ng katipunan na miminsang lumitaw noong Enero18,1896, na sa pagkalaganap ay nakaakit ng libu-libong kaanib ng samahan.

Si Emilio Jacinto ang naging matalinong katulong ni Andres Bonifacio sa pagtatatag ng Katipunan. Siya ang tinaguriang  “Utak ng Katipunan.” Pinamatnugutan din niya ang “Kalayaan” –ang pamahayagan ng Katipunan. Iniurong ni Bonifacio ang kaniyang sinulat sa Kartilya bilang paggalang sa tungkulin ni Jacinto bilang kautusan ng mga kaanib sa samahan. Narito ang ilan sa kaniyang mga sinulat.

-Kartilya ng Katipunan

-Liwanag at Dilim –Katipunan ng kanyang mga sanaysay na ma’y iba’t ibang paksa, tulad ng kalayaan, panggawa paniniwala, pamahalaan, at pag-ibig sa bayan.

-A Mi Madre (Sa Aking Ina) –isang madamdaming oda.
-A La Patria –ang ipinalalagay na kaniyang obra-maestra.


Buhat sa Liwanag at Dilim
Pinapag-alab ng layuning makaakit ng kaanib sa Katipunan, ni Jacinto ay nagsikap na sumulat ng mga kathang naglalahad ng mga katangian at mga karapatan na angkin, at dapat maangkin, ng tao. Sinisipi rito ang lalong makakabuluhan, na kumakatawan sa istilong maliwanag, kung payak man ni Jacinto.

Ang Ningning at Liwanag

Ang ningning ay nakakasilaw at nakakasira sa paningin. Ang liwanag ay kinakailangan ng mata, upang mapagwari ang buong katunayan ng mga bagay-bagay.

Ang bubog kung tinamaan ng nag-aapoy na sikat ng araw ay nagniningning, ngunit sumusugat sa kamay ng nagaganyak na dumampot.

Ang ningning ay madaya.
Ating hanapin ang liwanag, tayo’y huwag mabighani sa ningning. Sa katunayan ng masamang kaugalian: nagdaraan ang isang carwaheng maningning na hinihila ng kabayong matulin. Tayo’y magpupugay at ang isaisaloob  ay mahal na tao ang nakalulan. Datapua’y marahil naman ay isang magnanakaw, marahil sa isang malalim ng kaniyang ipinatatanghal na kamahalan at mga hiyas na tinataglay ay natatago ng isang sukaban.

Nagdaraan ang isang maralita na nagkakanghirp na pinapasan. Tayo’y napapangiti, at isasaloob: saan kaya ninakaw? Datapuat maliwanag nating nakikita sa pawis ng kaniyang noo at sa hapo ng kaniyang katawan na siya’y nabubuhay sa sipag, kapagalang tunay.

Ay! sa ating nangungugali ay lubhang nangapit ang pagsamba sa ningning at pagtakwil sa liwanag. Ito na nga ang magbayan ay namumuhay sa hinagpis at dalita.

Ito na nga ang dahilang isa pa na kung kaya ang tao at ang mga bayan ay namumuhay sa hinagpis at dalita.

Ito na nga ang dahilan na kung kaya ang mga loob na inaakay ng kapalaluan at ng kasakiman ay nagpupumilit  na lumitaw na maningning lalung-lalo na nga ang mga hari at pinuno na pinagkatiwalaan ng sa ikagiginhawa ng kanilang mga kampon, at walang nasa kungdi ang mamalagi sa kapangyarihan sukdang ikainis at ikamatay ng Bayan na nagbigay sa kanila ng kapangyarihang ito.

Tayo’y mapagsampalataya sa ningning, huwag nating pagtakahang ang ibig mabuhay sa dugo n gating mga ugat ay magbalat-kayong ningning.

Ay! kung ang ating dinudulugan at hinahayin ng puspos na galang ay ang maliwanag at magandang-asal at matapat na loob, ang kahit sino ay walang mapagpapaningning pagkat di natin pahahalagahan, at ang mga isip at akalang ano pa man, hindi hihiwalay sa maliwanag na banal na landas na katuiran.

Ang kaliluhan at ang katampalasanan ay humahanap nag ningning upang huag mapagmalas ng mga matang tumatanghal ang kanilang kapangitan; ngunit ang kagalingan at ang pag-ibig na dalisay ay hubad, mahinhin, at maliwanag na mapapatanaw sa paningin.

Ang lumipas na pinanginoon ng Tagalog ay labis na nagpapatunay na katotohanan nito.

Mapalad ang araw ng liwanag!

Ay! ang Anak ng Bayan, ang kapatid, ay matuto kaya na kumuha ng halimbawa at lakas sa pinagdaanang mga hirap at binatang mga kaapihan.

IBA PANG MAGHIHIMAGSIK
JOSE PALMA y VELASQUEZ (1876-1903)

Si Palma ay inianak sa Tundo noong ika-6 ng Hunyo, 1876. Kapatid siya ni Rafael Palma na naging pangulo ng Unibersidad ng Pilipinas. Nakatagpo ni Jose Palma, noong nag-aaral siya sa Ateneo de Manila, si Gregorio del Pilar na naging pinakabatang heneral ng mga hukbong manghihimagsik. Kahit bata pa sa Ateneo, si Jose Palma ay kumatha ng mga lirikong tula at pinahanga ang marami sa pamamagitan ng pagpapalimbag ng isang aklat ng mga tula noong siya’y 17 taong gulang pa lamang. Sumama sa himagsikan laban sa mga Amerikano. Ngunit kahit taglay niya ang damdamin at sigla ng paghihimagsik, ang mahina niyang katawan ay hindi sa higpit at hirap ng buhay sundalo, kaya’t ginugol niya ang kaniyang panahon sa panlilibang sa mga kawal ng manghihimagsik sa pamamagitan ng kanyang mga kundiman. Ang kanyang mga tula ay tinipon sa isang aklat na pinamagatang “Melancolicas” (mga panimdim) na inilathala ng kanyang kapatid na panahon ng mga Amerikano. Ngunit ang pinakadakilang ambag niya sa panitikang Filipino ay ang mga titik ng “Pambansang Awit ng Pilipinas” sa Kastila, na nilapatan ng musika ni Julian Felipe. Sinulat niya ang mga titik na ito habang ang pulutong ng kawal na kinabibilangan niya ay nakahimpil sa Bautista, Pangasinan. Ang mga letra ni Palma ay bihira ng awitin ngayon sapagkat ang salin sa Ingles ayTagalog ang siyang lalong gamitin, ngunit naito ang kaniyang orihinal. (Ang salin sa tagalog ay itinapat sa mga taludtod at hindi ang sariling inaawit).


“HIMNO NACIONAL FILIPINO”
(Pambansang Awit na Pilipino)

Tierra adorada
Hija del Sol de Oriente
Su fuego ardiente
En ti latiendo esta.

Lupang pinipintuho,
Anak ng Araw ng Silangan,
Ang apoy niyang naglilingas
Ay tumitibok sa iyo.

Patria de Amores,
Del heroism cuna,
Los invasores
No te hollaran jamas.

Bayan ng mga Pag-ibig,
Duyan ng kabayanihan,
Ang manlulusob
Ay di makayuyurak sa iyo kalianman.

En tu azul cielo, en tus aulas,
Entus montes y en tu mar,
Esplende y late el poema.

De tu amada libertad.
Tu pabellon, que en la slides
La Victoria ilumino,
No vera nunca apagados
Sus estrellas y su sol.

Sa bughaw mong langit, sa mga ulap mo,
Sa iyong mga bundok at sa dagat mo,
Kumikinang at tumitibok ang mga tulain
Ng itinatangi ming kalayaan.
Ang watawat mong sa mga paghahamok
Ang tinanglawan ng Tagumpay,
Ay di makikitang pagdimlan kailangan
Ng mga bituin mo’t ng iyong araw.

Tierra de dichas, de sol y amores,
En tu regazo dulce es vivir,
Es una Gloria para tus hijos,
Cuando te ofenden, por ti morir.

Lupaa ng ligaya, ng liwanag at mga
pag-ibig,

Sa kalungan mo’y kay-tamis mabuhay;
Ikiniluluwalhati ng iyong mga anak,
Na kapag inapi ka’y mamatay dahil
sa iyo.



PANITIKANG FILIPINO: KASAYSAYAN AT PAG-UNLAD (PANGKOLEHIYO)

ANG MGA PAHAYAGAN NG
PANAHON NG HIMAGSIKAN


Sa layuning maipaabot sa daigdig ang kahilingan at mithiin para sa bayan ng mga manghihimagsik nating mga kababayan, marami ring mga pahayagan ang naitatag at nalimbag nang panahon ng himagsikan.

-Heraldo de la Revolucion –naglalathala ng mga dekreto ng pamahalaang mapanghimagsik, mga balita, at akda sa Tagalog na pawang gumigising sa damdaming makabayan.

-La Independencia –pinamatnugutan ni Antonio Luna na naglalayon ng pagsasarili ng Pilipinas.

-La Republica Filipina –itinatag ni Pedro Paterno noong 1898.

-La Libertad –pinamatnugutan ni Clemente Zulueta.


