Marcelo H. del Pilar
Si Marcelo H. del Pilar ay kilalang-kilala sa kaniyang mga sagisag sa panulat na Plaridel, Pupdoh, Piping Dilat, at Dolores Manapat. Siya’y isinilang sa Cupang, Santo Nicolas, Bulacan noong ika-30 ng Agosto, 1850.
Ang kanyang mga magulang ay sina Julian H.del Pilar, kilalang manunulat sa Wikang Tagalog, at Ginang Biasa Gatmaitan. Kapatid niya ang paring si P. Toribo H.del Pilar na ipinatapon sa Marianas noong 1872. Marami silang magkakatid at dahil doon, ang ginawa ni Plaridel ay hindi tumanggap ng mana at ang nauukol sa kanya ay ibinigay sa mga kapatid.

Si Plaridel ay nagsimula ng pag-aaral sa kolehiyo ni G. Flores at pagkatapos ay lumipat sa Kolehiyo ng San Jose. Di nagtagal ay lumipat siyang muli sa Unibersidad ng Santo Tomas. Ang kanyang pag-aaral ng huling kurso ng derecho ay napatigil nang walong taon dahil sa pagkakagalit nila ng kura sa pag-aanak sa binyag sa simbahan ng San Miguel, Manila noong 1869. Noong 1880 ay nakuha rin niyang matapos ang karunungan sa pagiging manananggol.

Itinatag ni Plaridel ang pahayagang  “Diariong Tagalog” noong 1882, na pinaglathalaan niya ng mga puna at pansin sa hindi mabuting pamamalakad ng pamahalaang Kastila at dahil sa iba’t ibang kasalanang ibinuhat sa kanya na bunga ng paghihiganti ng mga prayle at upang maiwasan ang gagawing pagpapatapion sa kanya ay napilitang maglakbay sa Espanya noong 1888.

Naging katulong niya si P. Serrano Laktaw sa paglalathala ng naiibang pasyon at katesismo na kababasahan ng masasakit na biro sa mga prayl. Ginawa rin nila ang “Dasalan at Tocsohan” at ang “Kaiigat Kayo” galing sa salitang igat na isang uri ng isdang ahas na nahuhuli sa pullitika.

Nang dumating siya sa Espanya, hinalinhan niya si Graciano Lopez Jaena bilanng patnugot ng La Solidaridad na naging tagapamansag ng mga banal na mithiin na ikapagkakaroon ng mga kaluwagan sa sa pamahalaan ng mga Pilipino. Ngunit hindi tumagal nang mahabang panahon at napatigil siya dahil sa pagkakaroon niya ng karamdaman. Malubha niya siya at hindi halos makalakad ay nagtangka  pa ring makarating ng Hongkong upang doon man lamang ay mapakilos na niya ang kanyang kababayan. Ngunit nabigo siya sa tangka niyang ito. Sa sakit na pagkatuyo ay namatay siya sa Espanya, nguni’t bago binawian ng buhay ay ipinagbilin sa mga kasama na paratingin sa kanyang asawa’t mga anak ang sumusunod na  tagubilin “sabihin ninyo sa aking pamilya na sila ay hindi ko napagaalaman. Ibalita ninyo angg kapalaran n gating mga kababayan. Ipagpatuloy ninyo ang pagtuklas ng ginawa at kagalingan ng ating bayan.

Iyan angdahilan kaya si Plaridel ay may sariling pitak sa kasaysayan ng ating bayan. Hindi na mabilang ang mga lansangang nagtataglay ng kanyang pangalan. Ang dating bayan ng Kingwa ay ginawang Plaredil, ang mataas na paaralan sa Malolos ay Marcelo H. del Pilar High School, nguni’t higit sa lahat, mananatiling buhay habang panahon ang kanyang kagitingan at pagkamakabayan .

