ANG DULA SA BAGONG LIPUNAN
	Nanguna sa pagpapanibagong-buhay ng ating mga sinaunang dula ang ating naging Unang Ginang ng bansa na is Gng. Imelda Romualdez Marcos. Binuhay niya ang sarsuela na mga tagalog, sinakulo, at Embayoka ng mga muslim na pawang itinanghal sa ipinakumpuni niyang Metropolitan Theater at ipinatayong Folk Arts Theater at Cultural Center of the Philippines.

	Marami ring mga paaralan at samahan ang nagtanghal ng naiibang dula.

	Ang Mindanao State University ay nagtanghal sa Cultural Center of the Philippines ng isang dulang pinamagatang “Sining Kombayok”.

	Ang “Tales of Manuvu” na isang makabago o istilong rock na operang baalet ay nakadagdag din sa dulaang Pilipino noong 1977.
Ito’y tinampukan nina Celeste Legaspi, Leah Navarro, Hajji Alejandro, Boy Camara, Anthony Castelo, Rey Dizon, Gina Mariano, at iba pa. sinulat ito ni biebvenido Lumpera at ang koryograpu ay kay Alice Reyes.

	Si Imee Marcos na anak ang ating naging Pangulo ng bansa ay isa ring artista ng dulaan sa kaniyang pagkakaganap bilang pangunahing papel sa “Santa Juana ng Koral” at “The dairy of Anne Frank”.

Lalong nagpagsigla sa dula nang panahong iyon at maging sa kasalukuyan ang mga sumusunod na samahang pandulaan.

1.	PETA – nina Cecile Guidote at Lino Brocka
2.	Repertory Philippines – nina Rebecca Godines at Zenaida Amador
3.	UP Repertory – ni Behn Cervantes
4.	Treatro Filipino – ni Rolando Tinio

ANG RADYO AT TELEBISYON
	Ang radio ay patuloy pa ring itntangkilik nang paahong ito.
Ang kaniyang mga dugtungang dula tulad ng “Si Matar”, “Dahlia”,”ito  ang Palad ko,” “Mr. Lonley,” at iba pa ay siyang naging pampalipas oras o libangan pakinggan ng mga wala pang telebisyon. Maging ang mga awitin ay rito unang napakinggan ng ating mga kabataaan.

	Subalit maikakailan na maraming artista sa radio na lumapit sa telebisyon sa dahilang mas malaki ang bayad sa pagganap sa telebisyon kaysa radio. Ang ilan sa mga artistang ito ay sina Augusto Victa, Gene palomo, Mely Tagasa, Lina Pusing, Ester Chavez, Luz Fernandez, at iba pa.

	Ang mga dula sa telebisyon nang panahong ito na labis na tinangkilik ng marami ay ang Gulong Palad, Flor de Luna, Anna Liza, at iba pa.

	Ang “Superman” at “Tarzan” ay kinagiliwan din ng mga bata nang panahong ito.

ANG PELIKULANG PILIPINO
	Nagkaroon ng taunang Pista ng mga Pelikulang Pilipino sa panahong ito. Sa tuwing may ganitong kapistahan ay pawang mga pelikulang Pilipino lamang ang ipinalalabas sa mga sinehan sa Metro Manila. Ginagawaran ng gantimpala at pagkilala ang nagwa-waging mga pelikula at artista.

	Nagsilabas sa panahong ito ng Bagong Lipunan hanggang 1979 ang mga pelikulang walang romansa o seks subalit tinangkilik dahil sa kakaibang kayarian nito tulad ng:

	“Minsa’y …. Sa mga Kuko ng Liwanag” – sinulat ni Edgarde Reyes. Isinapelikula sa direksyon ni Lino Brocka at sa pangunguna ni Bembol Roco.

	Minsa’y Isang Gamu-gamo” – ang pangunahinh bituin dito ay si Nora Aunor.

	“Ganio Kami Noon … Paano Kayo Ngayon” – pinangunahan nina Christopher de Leon at Gloria Diaz.
	
	“Insiang” – pinangunahan ni Hilda Coronel.

	“Aguila” – pinangunahan nina Fernando Poe Jr, Jay Ilagan, at Christopher de Leon.

	Ngunit hindi pa rin napahindi nang panahong ito ang mga pelikulang nahihingil sa sex na kinagigiliwan ng mga tao. Mula sa mga inangkat na pelikula sa ibang banssa na natutungkol dito at maging ang ibang pelikulang Pilipino ay ito ang naging kasangkapan ng mga prodyuser sa pakita ng salapi mula sa takilya.

ANG MGA PAHAYAGAN KOMIKS, MAGASIN, AT IBA PANG BABASAHIN
	Sa panahong ito ng Bagong Lipunan, nagbihis ng panibagong anyo ang nilalaman ng mga pahayagan. Ang mga balitang dati’y naglalahad ng karahasan tulad ng patayan, nakawan, panggagahasa, at iab pa ay napalitan ng mga balitang pangkaunlaran, pang ekonomiko, disiplina, pangkultura, turismo, at iba pa. narito ang mga sumusunod na pahayagan.
