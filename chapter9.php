Panahon ng Bagong Lipunan

KALIGIRANG KASAYSAYAN
	Nagsimula ang panahon ng Bagong Lipunan noong ika-21 ng Setyembre, 1972. Nagpatuloy pa rin ang Gawad Carlos Planca sa pagbibigay  patimpalak.
	Halos tungkol sa ikauunlad ng bayan ang naging karaniwang paksain ng mga akda tulad ng Luntiang Rebolusyon (Green Revulotion), pagpaplano ng pamilya, wastong pagkain (Nutrition), “drug addiction,””polusyon,” at iba pa. pinagsikapan ng Bagong Lipunan na maputol ang malalaswang mga babasahin, gayundin ang mga akdang nagbibigay ng masasamang impluwensya sa moral ng mga mamamayan. Ang lahat ng mga pahayagang pampaaralan ay pansamantalang pinahinto, at maging ang mga samahang pampaaralan.

	Nagtatag ang pamahalaang malitar ng bagong kagawaran na tinawag na “Ministri ng Kabatirang Pangmadla” upang siyang mamahala at sumusubaybay sa mga pahayagan, aklat, at mga iba pang babasahing panlipunan.

	Muling naibalaik ng dating unang Ginang Imelda Marcos sa pagpapanibagong-buhay ang ating mga sinaunang dula tulad ng Senakulo, Sarsuela, Embayoka ng mga Muslim, at iba pa. ipinatayo niya ang Cultural Center of the Philippines, Folk Arts Theater, at maging ang Metropolitan Theater ay muli rin niyang ipinagawa upang magpagtanghalan ng mga dulang Pilipino.

PANITIKANG PILIPINO. KASAYSAYAN AT PAG-UNLAD (PANGKOLEHIYO)
	Naging laganap din ang pag-awit noon sa wikang Pilipino.
Maging ang mga ipinadadala sa ibang bansa ay awiting Pilipino rin ang inaawit.

	Ang mga lingguhang babasahin tulad ng Kislap, Liwayway, at iba pa ay malaki ang naitulong sa pagpapaunlad ng Panitikan.
Naging lagusan ito ng manunulat upang mailatahala ang kanilang mga akda.
	Tahasang masasabi na nagningning din ang panitakang Filipino nang panahong ito.
