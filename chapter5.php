Kasabay na nanaluktok sa larangan ng pagsulat ng nobela ni Lope K. Santos si Valeriano Hernandez Pena. Kilalang-kilala siya sa tawag na “Tandang Anong” at sa sagisag na “Kintin Kulirat”.  Ang kaniyang ipinalalagay na pinaka-obra maestra niya ay ang  “Nena at Neneng”.

INEGO ED. REGALADO
Si Inego Ed. Regalado ay ang anak ng isang tanyag na manunulat noong panahon ng Kastila sa sagisag na “Odalager”. Pinatunayan ni Regalado na hindi lamang siya nanalunton sa dinaanan ng kaniyang ama, kundi nakaabot pa sa karurukan ng tagumpay o “sumpong” sa panitik. Naging tanyag din siyang kuwentista, nobelista, at peryodista. Ang kalipunan ng kaniyang tula ay pinamagatang Damdamin.

ANG DULANG TAGALOG

Sa pagpusok ng Panahon ng mga Amerikano, sina Severino Reyes at Hermogenes Ilagan ay nagsimula ng kilusan laban sa moro-moro at nagpilit na magpakilala sa mga tao ng mga lalong kapakinabangang matatamo sa sarsuela at tahasang dula.

Ang mga taong hindi dapat malimutan sa larangan ng panitikang ito ay ang mga sumusunod:

Severino Reyes – “Ama ng Dulang Tagalog”, at may-akda ng walang kamatayang dulang “Walang Sugat”.

Aurelio Tolentino – ang ipinagmamalaking mandudula ng mga Kapampangan. Kabilang sa kaniyang mga dulang isinulat ay ang “Luhang Tagalog” na itinuturing  niyang obra-maestra, at ang “Kahapon, Ngayon at Bukas” na siya niyang ikinabilanggo.

Hermogenes Ilagan – nagtayo ng isang samahang “Compana Ilagan” na nagtanghal ng maraming dula sa kalagitnaang Luzon.

Patricio Mariano – sumulat ng nobelang “Ninay” at “Anak ng Dagat” na ipinalalagay na kaniyang obra-maestra.

Julian Cruz Balmaceda – sumulat ng “Bunganga ng Pating”. Ito ang nagbigay sa kaniya ng higit na karangalan at kabantugan.

ANG NOBELANG TAGALOG
Maganda rin ang naging kalagayan ng nobelang Tagalog nang Panahon ng Amerikano. Bukod kina Lope K. Santos at Valeriano Hernandez Pena, naging dakila rin namang nobelista sina Faustino Aguilar at Inego Ed. Regalado.

ANG MAIKLING KUWENTONG TAGALOG

Dalawang aklat na kalipunan ng mga kuwento ang napalathala noong Panahon ng mga Amerikano. Una’y ang “Mga Kuwentong Ginto” na napalathala noong 1936, at ang ikalawa’y ang  “50 Kuwentong Ginto ng 50 Batikang Kuwentista” noong 1939. Ang una’y inakda nina Alejandro Abadilla at Clodualdo del Mundo na naglalaman ng 25 pinakamabubuting kuwento, ayon sa kanila. At ng ikalawa ay kay Padrito Reyes. Ang “Parolang Ginto” ni Clodualdo del Mundo at “Talaang Bughaw” ni Abadilla ay napatanyag din nang panahong ito.

ANG TULANG TAGALOG

Lahat halos ng manunulat natin sa Tagalog nang Panahon ng Amerikano ay nakalikha ng magagandang  tula na sadyang napakahirap tarukin kung alin ang pinakamaganda. Palibhasa na kahit na sintanda ng kasaysayan ang pitak panulaan, sa tuwing ito’y lilitaw ay nagpapakita parin ito ng katamisan, kagandahan at kalamyuan.



MGA IBA PANG PANITIKANG FILIPINO
PANITIKANG ILUKANO

Ang mga sumusunod ay ang mga dapat kilalanin sa pagiging maningning ng panitikang Ilukano.
Pedro Bukaneg – “Ama ng Panitikang Ilokano”. Sa pangalan niya hinango ang salitang “Bukanegan” na nangangahulugan sa Tagalog ng Balagtasan.

Claro Caluya – “Prinsipe ng Mga Makatang Ilukano”. Kilala siya sa pagiging makata at nobelista.

Leon Pichay – kinilalang “pinakamabuting bukanegero”. Isa rin siyang ,makata, nobelista, kuwentista, mandudula, at mananaysay.

PANITIKANG KAPAMPANGAN

Kapag panitikang Kapampangan ang pinag-usapan, tiyak na nangunguna ang mga pangalan ng dalawang haligi nito.

	Juan Crisostomo – “Ama na Panitikang Kapampangan”. Ang salitang “Crisotan” na nangangahulugan ng “Balagtasan” sa Tagalog ay hinango sa kaniyang pangalan.

	Aurelio Tolentino – palibhasa’y nananalaytay sa kaniyang mga ugat ang dugong Kapampangan, ang kaniyang “Kahapon, Ngayon at Bukas” ay iginawa niya ng salin sa Kapampangan at pinamagatan niyang “Napon, Ngeni at Bukas”.


PANITIKANG BISAYA

	Narito naman ang mga pangalang nanaluktok sa panitikang Bisaya.

Eriberto Gumban – “Ama ng Panitikang Bisaya”. Nakasulat siya ng sarsuela, moro-moro, at mga dula sa Bisaya.

	Magdalena Jalandoni – nag-ukol naman ng panahon sa nobelang Bisaya. Isinulat niya “Ang Mga Tunuk San Isa Ca Bulaclac”.

ANG PANITIKANG FILIPINO SA INGLES
Nagsimulang ituro sa mga paaralang Filipino ang Ingles noong 1900. Mula sa taong ito hanggang 1930 ay maraming naisulat na mahahalagang sanaysay, maiikling kuwento, at mga tula. Ang ilan sa mga sanaysay ay madaling unawain dahil pawang mga katatawan subalit ang iba nama’y nauukol sa mga paksang pormal, tulad ng tungkol sa edukasyon, kasaysayan, pulitika, at mga suliraning panlipunan.

	Mapapansin na ang mga kuwento noon ay kadalasang tungkol sa pag-ibig at ang mga paksa at pangyayari ay pawang ginaya lamang.

Sa larangan naman ng tula, pinunang pawang mga mukhang artipisyal ang gayon sa paglalarawan ng mga makatang amerikano. Sa madaling salita, walang orihinalidad ang mga akdang pampanitikang naisulat noong panahon ng Amerikano.

Narito Ang Ilan Sa Ating Mga Manunulat:
Jose Garcia Villa – pinakatanyag na Pilipinong manunulat sa Ingles sa larangan ng maikling katha at tula. Kilala rin siya sa sagisag na “Doveglion”.

Jorge Bocobo – isang mananaysay at mananalumpati. Ilan sa kaniyang mga sinulat ay ang “Filipino Contact with America, A Vision of Beauty” at “College Education”.

Zailo Galang – sumulat ng kauna-unahang nobelang Pilipino sa Wikang Ingles “ A Child of Sorrow”.

Angela Manalang Gloria – umakda ng “April Morning”. Nakilala siya sa pagsulat ng mga tulang liriko noong panahon ng Komonwealth.
Zulueta de Costa – nagkamit ng unang gantimpala sa kaniyang tulang. “ Like The Molave” sa Commonwealth Literary Contest noong 1940.

NVM Gonzales – may-akda ng “My Islands” at “Children of the Ash Covered Loom”. Ang huli ay isinalin sa iba’t ibang wika sa India.

Estrella Alfon – pinalalagay na pinakapangunahing manunulat na babae sa Ingles bago magkadigma. Siya ang sumulat ng “Magnificence” at “Gray Confetti”.

Arturo Rotor – may akda ng “The Wound and the Scar” na siyang kauna-unahang aklat na nalimbang sa Philippine Book Guild.















