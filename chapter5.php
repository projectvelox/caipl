 KALIGIRANG PANGKASAYSAYAN
Ang mga Pilipinong mapanghimagsik ay nagwagi laban sa mga Kastila na sumakop sa atin nang higit sa tatlong daang taon. Naiwagayway ang ating bandila noong ika-12 ng Hunyo, 1898, tanda ng pagkakaroon natin ng kalayaan. Nahirang si Hen. Emilio Aguinaldo noon bilang unang pangulo ng Republika ng Pilipinas, subalit ang kalagayang ito’y naging panandalian lamang sapagkat biglang lumusob ang mga Amerikano. Nagkaroon ng digmaang Pilipino-Amerikano na siyang naging sanhi ng pagsuko ni Hen. Miguel Malvar noong 1903. Gayun pa man, ang kilusang pangkapayapaan ay simula noong pang 1900. Maraming Pilipino noon ang nagsalong ng sandata at muling nanulat, sapagkat ang diwa at damdaming makabayan ng mga kababayang ito ay hindi nakuhang igupo ng mga Amerikano, bagkus ay lalong naging maalab pa.
Pinasok ng mga manunulat  na Pilipino ang lahat ng larangan ng panitikan tulad ng lathalain, tula, kuwento, dula, sanaysay, nobela, at iba pa. Maliwanag na mababasa sa mga akda nila ang pag-ibig sa bayan at pag-asam ng kalayaan.
Ang masiglang kilusan sa larangan ng panitikan ay nagsimulang mabasa sa mga sumusunod na pahayagan.
1.	Nuevo Dia (Ang Bagong Araw) – itinatag ni Sergio Osmena noong 1900. Makalawang pinatigil ng mga sensor ng Amerikano ang paglathala nito at binalaan si Osmena at ang kaniyang mga kasamahan na ipatatapon dahil sa mga lathalaing makabayan.

2.	El Grito del Pueblo (Ang Sigaw Ng Bayan) –itinatag ni Pascual Poblete noong 1900.
3.	 El Renacimiento (Muling Pagsilang) –itinatag ni Rafael Palma noong 1900.
	Marami ring mga dula ang naisulat noon, ngunit sa una o ikalawang pagtatanghal pa lamang sa Teatro Zorilla ay ipinatigil din ng mga Amerikano dahil sa diwang makabayan pa rin ang pinapaksa. Kabilang dito ang mga sumusunod:
1.	Kahapon , Ngayon At Bukas – sinulat ni Aurelio Tolentino. Naglalahad ito ng panlulupig ng mga Amerikano at ang tangka nila ng manakop sa Pilipinas.
2.	Tanikalang Ginto – ni Juan Abad
