AMADO V. HERNANDEZ

	Si Amado V. Hernandez ang tinaguriang  “Makata ng Mga Manggagawa” sa ating panitikan sa dahilang nasasalamin sa kaniyang mga tula ang marubdob na pagmamahal sa mga dukahang manggagawa. Para sa kaniya, ang tula ay halimuyak, taginting, salamisim, aliw-iw. Ang panitik ay makapangyarihan at ayon sa kaniya pati hari ay napayuyuko ng panitik. Marami siyang  naihandog sa panitikan tulad ng Isang Dipang Langit, Mga Ibong Mandaragit, Luha ng Buyawa, Bayang Malaya, Ang Panday, Munting Lupa at iba pa, ngunit ang pinaka-obra-maestra niyang isinaalang-alang ay ang tulang “Ang Panday”. Tunghayan natin ang nasabing tula.

“ANG PANDAY”
