PANITIKAN SA PANAHONG ITO
Tatlong pangkat ng mga manunulat ang kumatawan sa Panitikang Filipino ng panahong ito. Sa mga unang taon ng panahon ng mga Amerikano, ang mga wikang ginamit sa panulat ay Kastila at Tagalog, at ang mga sari-sarili at katutubong wika sa mga lalawigan, ngunit Kastila at Tagalog ang namayani. Sa may dakong 1910, isa na namang bagong pangkat ng mga manunulat ang nagsimulang magpahayag sa Ingles.
Bagama’t nagkakaisa sa kaisipan at diwa ang nasabing tatlong pangkat ng mga manunulat ay nagkakaisa naman sila sa hilig ng pamamaksa at pamamaraan. Ang mga manunulat sa Kastila ay naging mahilig sa pagpapahayag ng damdaming makabayan at pagpaparangal kay Rizal at sa iba pang naging bayani ng lahi. Ang mga manunulat sa Tagalog ay nagpatuloy sa maligayang pagpapahayag na may daing sa kaapihan ng bayan at may pagpapasigla sa pagmamahal at pagtatangi sa sariling wika. At ng mga manunulat sa Ingles ay nagtataglay ng mababaw na panunulad sa pamamaksa at pamamaraang Amerikano. Ngunit sa kalahatan, ang Panitikang Filipino sa panahong ito ay sumusunod sa romantisismo ng Europa. Naging palasintahin ang himig ng halos lahat ng mga naisulat na sa bayan, sa kapwa, at iba pa.
	PANITIKAN SA KASTILA
Naging inspirasyon ng ating manunulat sa Kastila si Rizal hindi lamang sa kanyang pagiging makabayang lider kundi dahil pa rin sa kaniyang naisulat na dalawang nobelang Noli at Fili. Sinasabing ang dalawang nobelang ito ang nagtataglay ng pinakamahusay na katangian sa lahat ng naisulat na nobelang pampanitikan, maging sa Ingles at Filipino. Kaya’t ang mga nagaganyak  sumulat sa Kastila ng mga pagpuri sa kaniya at sa iba pang mga bayani ay sina Cecilo Apostol, Fernando Ma. Guerrero, Jesus Bal-mori, Manuel Bernabe,Claro M. Recto, at iba pa.
