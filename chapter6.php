ANG MGA DULA SA PANAHONG ITO
Nagkaroon ng puwang ang dulang tagalog nang Panahon ng Hapon dahil napinid ng mga sinehang nagpapalabas ng mga pelikulang Amerikano. Ang mga malalaking sinehan ay ginawa na lamang tanghalan ng mga dula. Karamihan sa mga dulang pinanalabas ay salin sa Tagalog mula Ingles. Ang mga nagsipagsalin ay sina Francisco Rodrigo, Alberto Cacnio, at Narciso Pimentel. Sila rin ay nagtatag ng isang samahan ng mga mandudulang Pilipino na pinangalanan nilang “Dramatic Philippines”. Ilan sa mga nagsisulat ng dula ay ang mga sumusunod:

1.	Jose Ma. Hernandez – sumulat ng “Panday Pira”
2.	 Francisco Soc. Rodrigo – sumulat ng “Sa Pula sa Puti”

3.	Clodualdo del Mundo – sumulat ng “Bulaga”

4.	Julian Cruz Balmaceda – sumulat ng  “ Sino ba Kayo” “Dahil sa Anak” “Higanti ng Patay”



MGA MAIKLING KUWENTO SA PANAHONG ITO

	
Naging maunlad ang larangan na maikling kuwento noong Panahon ng Hapon.
	
	Maraming nagsisulat ng maikling kuwento. Kabilang dito sina Brigido Batungbakal, Macario Pineda, Serafin Guinigundo, Liwayway Arceo, Narciso Ramos, NVM Gonzales, Alicia Lopez Lim,
Ligaya Perez, Gloria Guzman at iba pa.

	Ang pinakamahusay na akda ng taong 1945 ay pinili ng lupon ng mga inampalan na binubuo nina Francisco Icasiano, Jose Esperanza Cruz, Antonio Rosales, Clodualdo del Mundo, at Teodor Santos. At ang 25 maikling kuwentong pinili ay pinasuri naman kina Lope K. Santos, Julian Cruz Balmaceda, at Inigo Ed. Regalado. Ang kinawakasan ng pagsusuri ay nagsasabing ang mga sumusunod ang nagkamit ng unang tatlong gantimpala:


