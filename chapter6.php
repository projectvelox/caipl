Chapter 6
Panahon Ng Mga Hapones

KALIGIRANG KASAYSAYAN
	
	Ang Panitikang Fiipino sa wikang Ingles sa pagitan ng taong 1941-1945 ay nabalam sa kaniyang tuluy-tuloy na sanang pag-unlad nang muli tayong sakupin ng isa na namang dayuhan mapantil ang mga Hapones. Natigil ang panitikan sa Ingles. Maliban sa Tribune at Philippine Review, ang lahat halos ng pahayagan sa Ingles ay pinatigil ng mga Hapones.

	Naging maganda naman ang bunga nito sa Panitikang Tagalog Patuloy na umunlad ito sapagkat ang mga dating sumusulat sa Ingles ay bumaling sa pagsulat sa Tagalog. Si Juan Laya na dating manunulat sa Ingles ay nabaling sa pagsulat sa Tagalog, dahil sa mahigpit na pagbabawal ng pamahalaang Hapon tungkol sa pagsusulat ng anumang akda sa Ingles.

	Ang lingguhang Liwayway ay inilagay ng mga Hapones sa mahigpit na pamamatyag hanggang sa ipabahala ito sa isang Hapong nagngangalang Ishikawa.

	Sa madaling salita, nabigyan ng puwang ang Panitikang Tagalog nang panahong ito. Marami  ang mga nagsisulat ng dula, tula, maikling kuwento, at iba pa. Ang mga paksain ay pawang natutungkol sa buhay lalawigan.

ANG MGA TULA SA PANAHONG ITO

	Ang karaniwang paksa ng mga tula noong Panahon ng hapon ay tungkol sa bayan o sa pagkamakabayan, pag-ibig, kalikasan buhay lalawigan o nayon, pananampalataya, at sining.

	Tatlong uri ng tula ang lumaganap sa panahong ito. Kinabibilangan ito ng:
a.	Haiku – isang tulang may malayang taludturan na kinagiliwan ng mga Hapones. Ito’y ay binubuo ng labimpitong pantig na nahahati sa tatlong taludtod. Ang unang taludtod nito ay may limang pantig, ang ikalawa ay pitong pantig, at ang ikatlo ay limang  pantig ang una. Maikli  lamang ang haiku, ngunit nagtataglay ng masaklaw ng mga matatalinhagang kahulugan.

b.	Tanaga – tulad ng haiku, ito’y maikli ngunit may sukat at tugma. Ang bawat taludtod nito ay may pitong pantig. Nagtataglay din ng mga matatalinghagang kahulugan.

	k. Karaniwang anyo – ang mga katangian nito ay natalakay na sa panimulang pag-aaral ng aklat na ito.
	
	Narito ang halimbawa ng haiku, tanaga, at karaniwang anyo ng tula:



