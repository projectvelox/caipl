Narito ang buod ng bawat isa:

LUPANG TINUBUAN 
Ni Narciso Reyes

Ang kuwento ay nagsimula sa istasyon ng tren. Sa paglalarawan ng kapaligiran ay makikita at maririnig ang iba’t ibang ingay ng kapaligiran tulad ng pagsigaw ng mga batang nagtitinda ng diyaryo, pagbibilin ng mga magsisiuwi sa probinsya, at pangungumusta sa mga kaanak sa lalawigan.

Si Dandin ay uuwi sa probinsya kasama ang kaniyang Tiya Juana at Tiyo Gorio.

Sila ay tutungo sa lalawigan upang makipaglibing sa kanyang Tata Inong na pamangkin ng kanyang Lola Asyang at pinsan ng ama niya. Itinanong ni Danding sa kanyang Tiya Juana kung anu ang itsura ng Malawig na siyang baying sinilangan at pinaglakhan ng kaniyang ama.

Ang Malawig ay tulad din ng ibang Nayon sa Luzon na may makikitid, paliku-liko, at maalikabok na daan. May mga puno ng Akasya, manga, niyog, at kawayan. Ang bahay roon ay yari sa pawid. Walang maganda roon kundi langit, sabi ng kutsero ng karitelang sinakyan nila.

Pagdating nila roon ay kay-rami pala nilang kamag-anak at halos napudpod ang kaniyang sarat na ilong sa kakahalik ng kamay. Halaos pala naroroong mga bata’t matatanda ay kamag-anak niya at halos di-matapus-tapos ang pagpapakilala sa kaniya.

Pagdating doon sa itaas ng bahay ay lumapit siya sa kabaong at namalas niya ang pagkakahawig nito sa kaniyang ama.

Pagkapananghali ay nagtungo siya sa bukid at nakita niya si Lola Tasyo. Sinabi ng kaniyang Lola Tasyo na ang ugali niya ay tulad ng kaniyang amang di-mahilig sa maraming tao. Isinalaysay niya ang kabataan ng kaniyang ama. Ang pagsasaranggola sa bukid, ang pagkahulog sa kalabaw, ang pagtatago sa Kastila, ang pagsulat ng tula, ang dalaga sa bunton ng palay, na siyang naging sanhi ng pagkaluwas niya sa Maynila.

Unti-unting pinutol niya ang pakikipag-usap sa matanda upang magbalik sa bahay.

Kinabukasan ay inilibing ang patay sa isang libingan na nasa gilid ng simbahan, bagay na nagpapagunita kay Danding sa sumpa ng  Diyos kay Adan at sa mga anak nito. Nagunita niya na sa bakuran ding ito nahihimlay ang alabok ng kaniyang mga ninuno, at mga labi ng katipunan, kaya sinikap niyang huwag masaling ang maliliit na halamang naroroon.
Bago tuluyang inihulog ang bankay ay binuksang muli ang takip upang masulyapan ang mga naulila at nabasag na muli ang katahimikan ng mga impit na hikbi ng mga ulila. Naluha rin si Danding.

Nagpaunang umuwi si Danding at nagtuloy sa bukid at muling ginunita ang kabataan ng kaniyang ama. Marahan siyang natawa.

Sa sandaling iyon ay lihim na nadama ni Danding ang pag-ibig sa lupang tinubuan. Doon siya naunawaan kung bakit ang pagpapatapon sa ibang bansa ay napakabigat na parusa, at kung bakit ang nawawalay na anak ay sumasalunga sa bagyo makauwi lamang sa Inang Bayan. Kung bakit walang atubiling naghain ng dugo sina Rizal at Bonifacio.


UHAW ANG TIGANG NA LUPA
(1943) Liwayway Arceo

Ang kuwento ay tungkol sa uhaw na pagmamahal ng anak sa kaniyang magulang at uhaw na pagmamahal din ng ina sa kanyang asawa na inihahambing sa pagkauhaw ng tigang na lupa sa tubig, sa hamog.

Naging kapansin-pansin ang ilang gabi nang hindi pagkatulog ng ina. Malalim ang kanyang paghinga at palaging malungkot kung tumitig at kung minsan ay may impit na paghikbi.
Ilang araw naring hindi niya nadadalaw ang aklatan at ilang araw ang hindi niya nasasalamin ang isang larawang mahal sa kanya. May bilugang mukha, malapad na noo, at singkit na mga mata. Ang kaniyang noo at mata ay kuha sa ama niya samantalang ang ilong niya na parang tuka ng loro at maninipis na labi ay kaniyang ina.

Hindi palakibo ang kaniyang ina. Matipid siyang mangusap. Bihira siyang magalit at maikli ang kaniyang pananalita, Pag sinabi niyang, “lumigpit ka!” ay di ka niya ibig Makita. Ang ngiti niya ay parang patak ng ulan kung tagaraw. Ang batang puso ng anak ay tigang na lupang uhaw na uhaw.

Ni minsan di-niya nakita na nakagalit ang kaniyang ama’t ina. Marahil ay sapagkat kapwa sila laging nagbibigayan sa isa’t isa.

Kung minsan ay hinahanap niya niya ang pagkukuwento ng isang ama at pagmamasid ng isang ina at may mga batang masasayang nakikinig.

Sa halip ay lagi niyang minamalas ang ama na nagsasalita sa kaniyang pagmamakinilya, sa kaniyang pagbabasa. Minamasdan niya ang pagkamot ng noo nito, ang pagbuga ng sigarilyo at kung paano mag-isip at magpatuloy sa pagsulat.

Ang kaniyang ina ay isang magandang tanawin kung nanunulat at nagbuburda at para siyang kapana-panabik na kuwento ngunit ito ay napapawi.
Nababagot ang anak sa kaniyang pag-iisa, nananabik siya sa isang sanggol na kapatid, o isang mapaghihingahan ng sama ng loob.

Kung hindi man nagkakagalit ang kaniyang ama’t ina ay hinahanap rin niya ang magiliw na paglalambingan o pagbibiruan.

Kung nagpapaalam ang ama ay isang sulyap na, “alis na ako” at isang panakaw na sulyap.

Mabibilang sa daliri ang pamamasyal ng mag-anak. Malimit ay ang ina ang kasama hindi rin namamasyal ang mag-asawa.

Inuumaga ng pag-uwi kung minsan ang ama niya. Ngunit di-kakapansinan ng ibang kilos ng ina. Wala kang maririnig na pagsisisi sa ina.

Ilang taon na ngayon ang nakaraan nang minsan may ibinalik na maliit na aklat ang tagapaglaba; nakuha niya sa lukbutan ng ama at ibinigay sa ina yaon ang talaarawan.

Kinabukasan may bakas na luha sa mata ng ina at lalong di naging palakibo at malungkot. Anu ang nasa talaarawan?

Isang gabi nalasing na naman ang ama, kakaiba ang kalasingan ngayon at hinilamusan ng ina hindi kumikibo ngunit nasa mata ang pagtutol. Idinaing ng ama ang dibdib at ulo at sinabi ng ina na marahil ay sipunin ka.

Isang panyong basa ng malamig na tubig ang itinali ng anak sa ulo ng ama at binuhusan ng mainit na tubig na pinaglagaan ng dahon ng alagaw at nakangiting sinabi ng ama na: Manggagamot pala ang aking dalaga!

Ngumiti ang dalaga at naisip niyang sana’y siya sa kaniyang ina sa mga sandaling yaon.

Naratay ang ama nang ilang araw. Hindi humiwalay ang ina. Ayaw ipagtapat ng anak ang tunay na karamdaman nito.

Ipinaayos ng ama sa anak ang kanyang hapag. Idinikit ang kuwentong kalalathala pa lamang. May nakita siyang pelus na rosas at isang salansan ng nakasalansan na mga liham. Nakatitik ang pangalan ng ama at tanggapan ng mga sobre.

Ang larawan sa kahitang pelus ay hindi yaong hawas na mukha at ilong na parang sa loro at nakasulat ang:Sapagkat ako’y hindi nakalimot. Walang lagda ang larawan at bigla ang dalaga ay napoot at naghihinakit sa ama.

Sa isang liham ay nakasaad ang sumusunod: Na sila ay pinaglapit ng tadhana sa isang pagkakataong mayroon nang sagwil o hadlang sa kanilang kaligayahan. Wala na sila sa gulang ng kapusukan upang dayain ang kanilang mga damdamin ngunit mayroon nang nakapagitan sa kanila kaya sa pangarap na lamang nila ito bubuhayin at sana’y huwag na silang magising sa katotohanan…

Nakita siya ng babae sa panaginip; sinusumbatan siya ngunit di niya balak magwasak ng tahanan sapagka’t ang sinumang bahagi ng kaniyang buhay ay mahal rin niya at di dapat paluhain.

Ang pag-ibig nila ay isang dula na sila ang pangunahing tauhan: sila ang nagsimula at sila ang magwawakas. Humihiling lamang siyang tulungang pawiin ang kalungkutang halos papatay sa kanya.

Nadama na lamang ng anak ang kamay na ina sa balikat di niya namalayang pumasok ito sa aklatan. Nakita niya ang larawan sa kahitang pelus na rosas. Natunghayan din niya ang liham. Dumating at umalis ang ina na walang nasambit ni isang kataga. Tanging ang bigat ng kaniyang kamay sa balikat ng anak ang nadama. Lalong nanahimik ang ina at iniwasang magsalubong ang titig ng anak ngunit nakabadha ang labis na kalungkutan.

Hiningi ng ama ang mga panulat sa aklat-talaan ngunit nang sinabi ng anak na makasasama sa kanya ay sinabing, Ngayon ang aking anak ay susulat ng ukol sa akin. Ibig niyang tumutol ngunit di niya iyon nabigkas.

Nasa kalamigan ng lupa ang kaluwalhatian ko! ang ama.

	Huwag kang padala sa simbuyo ng iyong kalooban, ang unang tibok ng puso ay hindi pag-ibig sa tuwina – Halos kasinggulang siya ng anak nang pakasalan niya ang ina ng dalaga. Labing walong taong gulang at ayon sa kanya kung minsan ay ikaw narin ang nagbibigay ng kalungkutan o pahirap sa iyong sarili at dalahin habang buhay.

	Malimit ng mawala ang diwa ni ama samantalang ang ina ay patuloy sa di pagkibo, di pagkain, di pag-idlip, patuloy sa pagluha kung walang makakita.

	Dumantay ang kamay ng ina sa noo ng ama at pinatakas ang tinitimping sama ng loob sa pagdadaop ng ngipin at labi nito.

	Nagsalita ang ama… Magaling na ako, Mahal ko, maaari na tayong tumungo… ang moog na kinabibilangan ko’y aking wawasakin…sa ano mang paraan.

	
Naluha ang ina at pumatak sa bisig ng ama. Iminulat ng ama ang mabigat na talukap ng mga mata at nagkatitigan sila ng ina.

Hawak ng ina ang kamay ng ama nang muli itong magsalita… Sabihin mo mahal, mahal ko, na maaangkin ko na ang kaligayahan ko…

Kinagat ng ina ng mariin ang mga labi at sinabing! Maaangkin mo na, Mahal Ko!

Hinagkan ng ina ang ama at sabay ng paglisan ng isang kaluluwa at wala nang luhang dumaloy sa mga iyon.

Tiyak niyang liligaya na ang kaluluwa nito.

ANG PANITIKANG FILIPINO
SA WIKANG INGLES NG PANAHONG ITO

Kung baga sa langit, naging makulimlim ang Panitikang Filipino sa wikang Ingles noong panahon ng Hapon dahil sa mga mahigpit na pagbabawal ng mga Hapones ng pagsulat at pagtatanggap ng akda sa Ingles. Ilan lamang ang naglakas ng loob nito at kabilang sa mga nagsisipagsulat ay sina Salvador Lopez,Francisco Icasiano, Federico Mangahas, Manuel Aguilla, Carlos P. Romulo, at Carlos Bulasan.

Mga Tulong Sa Pag-aaral

I.Sagutin ang mga sumusunod:
1. Ano ang naging kalagayan na Panitikang Ingles nang Panahon ng Hapon?
2. Ano ang nangyari sa Panitikang Tagalog nang Panahong ito?
3. Talakayin ang mga naging paksa ng tula nang panahon ng Hapon?
4. Anu-ano ang tatlong uri nang tulang lumaganap nang panahong ito? Magbigay ng pahayag sa bawat isa.
5. Talakayin ang naging kalagayan ng dulang Tagalog nang panahong ito.
6. Anu-anong mga dula ang naisulat nang panahong ito at sinu-sino ang mga nagsisulat?
7. Ano naman ang nangyari sa maikling kuwento nang panahon ng Hapon? Talakayin.
8. Sinu-sino ang mga naging lupon ng inampalan sa pagpili ng pinakamahusay na akda sa larangan ng maikling kuwento nang panahong ito?
9. Turan  ang unang tatlong gantimpalang nagkamit sa larangan ng maikling kuwento nang panahon nang Hapon.









