ANG DULA, MAIKLING KWENTO, AT NOOBELA SA PANAHONG TIO
	Labis na naging mapangahas ang mga manunulat ng dula, maikling kwento o maging ng nobela sa panahong ito, hindi lamang sa paksa kundi maging sa usapan o salitaan ng kanilang mga tauhan sa akda.
	
	Payak ngunit makatotohanan ang salitaan o lengguwaheng ginagamit nila ngunit kadalasa’y ang payak at makatotohanang usapan o lengguwaheng ito, bagamat nasa makabagong panahon na ay hindi parin makayanang basahin nang hindi pamumulahan ng mukha ng mga babaeng may bakas pa ni Maria Clara, lalo’t ang usapan ay usapang lalaki o ginagamit sa tagpo sa pagtatalik.
ANG MGA PELIKULA AT KOMEKS
	Nang panahon ding ito ng aktibismo, nagsimula napanood ang mga pelikulang malalaswa na nakasisira sa kaugaliang silangalin – ang tinatawag na mga Pelikulang Bomba.








