KABANATA VIII
Panahon ng Aktibismo

Kaligirang Kasaysayan
	Ang pagiging Malaya sa turing ng mga Pilipino ay hindi maatim tanggapin ng ilang mga mamamayan Pilipino lalo nang mga Kabataan. 

	Naging mainit ang pamamalasak ng mga kabataan noong 1970-72 sa mutsaring paniniwala ang dahilan ng kanilang pagiging aktibista.

	Sa masusing pagmamasid at pag-aaral sa takbo ng pamahalaan marami sa ating kabataan ang naniniwalang di na “demokratiko” kundi isa ng “gobyernong kapitalista” ang umiiral sa ating bayan sapagkat damang-dama raw nila ang lalong paghihirap ng mga mahihirap at lalong pagyaman ng mga mayayaman. Ang iba naman ay patuloy na nanalig na matatag ang pamahalaan dimokratiko at mga tao lamang na nag papatakbo ng pamahalaan ang mga kakulangan. At ang iba naman ay may paniniwalang dapat ng palitan ng “ Sosyalismo” o “Komonismo” ang bulok na pamahalaan ibat-ibang samahan ang naitatag at nasapian ng ating mga kabataan ng panahong ito may mga kabataang kapabilang sa bagong hukbo ng bayan ( New People’s Army),  may mga naging Burgis Radikal o rebelde at may roon ding mga nanatiling parang mga walang pakialam sa takbo ng pamahalaan.

	Sa kalahatan,maraming mga kabatan ang naging aktibista upang humingi ng pagbabago sa takbo ng pamahalaan. Subalit sa kanilang pamamahayag hinggil sa pagbabagong ito na dala narin. Marahil ng matinding damdaming makabayan at upang mabigyang diin na rin ang kahalagahan ng kanilang kahilingan ay naging matalim at mabalasik ang panunulat ng ilang nating kabataan. At ang namgapiit sa mga kampong military ng bansa.

	Marami ring akda ang naisulat sa panahong ito, ngunit dahil sa ang mga akda mahigpit na ipinagbawal sa una palang napaglalathala at karamihan sa mga umakda’y kailangan lapitan pat makapanayam, ang pangangalap at pagpapahalaga ng mga akdang ito ay ipinauubaya na nang mga naghanda ng aklat na ito sa mga mananaliksik at palaaral.


ANG BINHI NG AKTIBISMO
	Humantong sa pagkaka “Deklara” ng batas militar (martial Law) noong 1972 ang binhi ng aktibismo. Ngunit masasabing ang binhi ay naihasik na sa mga kabataan maging noong mga panahon pa ni Lapu Lapu, Lakandula, Rizal, at iba pa. sadya paring masasabi na monopolohiya ng kabataang may init na dugong dumadaloy sa kanilang ugat ang dahilan ng paghihimagsik laban sa makapangyarihang lakas ng Pilipinas. Kaya’t balido ang sinabi ni Rizal na ang mga kabataan ang pag-asa ng bayan.

PANAHON NG DUGUANG PLAKARD
	Wika ni G. Ponciano Peneda sa kanyang aklat na pinamagatang “Ang Panitikang Pilipino sa Kaunlaran ng Bansa” ito ang panahong minsan pang pinatunayan ng kabataang Pilipino na hindi lagging pagyuyuko ng ulo at pag ilag sa hangin ang bumubuo ng kanyang pagkalahi at pagka bansa. 
	May sukdulan ang kanyang pagtitimpi, sumasabog parang taal kung puno sa ngitngit ang matimping kalooban.
	Dugo? Ano ang dugo nang isang tao kung ihahambing sa dugong ibinubu upang ikulay sa pula ang ating bandila? 
	Buhay? Ano ang buhay kung itatapat sa habang panahong hintuturong nakatundos sa mukha ng isang duwag at di nagkakaroon ng paninindigan para sa sarili at gayun din sa kasunod na salin lahi?
	 Sa kubuuoan maraming kabataan ang nag buwis ng buhay. Nagpamalas ng buong giting sa pagtatanggol ng karapatan ng masang Pilipino, walang takot  na suungin ang kamatayan basta maipaglaban lamang ang mga Prinsipyu at tunay na karapatan.
ANG KALAGAYAN NG PANITIKAN SA PANAHONG ITO
	Naging ganap na mapanghimagsik ang mga kabataan ng panahong ito. Ito’y mapapatunayan hindi lamang sa madugo at mapangwasak na dimonstrasyon at mga pagpapahayag. Ang mga pahayagan ng mga mag-aaral sa kani-kanilang pamantasan ay punong-puno ng damdaming mapanghimagsik. Ang mga dating aritoskratang manunulat ay nagkaroon ng kamulatang panlipunan. At maliban sa makinilya at gumamit din sila ng pinsel at isinulat sa mga placard, sa pialng pintura ang mga kaugnay na salitang nagpapahayag ng karaingan at pakikibaka.
	Tinalakay nila ang kabulukan ng lipunan at pulitika. Ang alinmang establisimento ay naging sagisag ng kabulukang dapat baguhin. Madarama sa simbahan, sa paaralan, at maging sa tahananang lason ng kawalang pag-asa ng mga kabataan sa pamahalaan. Maging ang mga pari, guro at mga magulang bilang awturidad o mga dapat taong dapat igalang ay niyanig ng mga kabataang radikal bilang kalaban na pabigat sa hinihingi nilang pagbabago.

	Humangga ang panitikang ito ng mga aktibista sa pagsasaad ng dapat gawin upang lutasin ang suliranin.
	Ang ilan sa mga kabataang bumandila sa panitikang rebulusyunaryo ay sina Rolando Tinio, Rogelio Mangahas, Efren Abueg, Rio Alma, Clemente Bautista, at iba pa.

ANG PANULAANG FILIPINO SA PANAHON NG AKTIBISMO
	Masasabing halos nagtataglay ng tatlong katangian ang mga tulang naisulat ng mga batang makata at mananalumpati sa panahong ito ng aktibismo. Una ay ang pagmamasid at pagsusuri sa kalagayan ng bayan; pangalawa ay ang pagsisiswalat ng mga katiwalian at dayukdok na pagpapasasa ng mga nanunungkulan at ang ikatatlo ay ang tahasang masasabing labag sa kagandahang- asal na panunungayaw at karahasan sa pananalita.
	Ang mga sumusunod ay ilan lamang sa naging akda sa panahon ng aktibismo.
Tunghayan natin ang ilang saknong mula sa tula ni Rio Alma na nagpapahayag ng matimping pagngangalit ng damdamin.

	“Marahil dahop ang diwa ko upang isaulo’t ipaliwanag.
	Ang panaginip at kamatayanng sanlaksang anak-pawis”
	“Saksi ako sa palahaw ng mga dalagitang tila kinakatay na baboy
	Habang ginagahasa ng mga hayok na pulitiko’t negosyante”
	“Sa sabuyan ng putik ng mga kongresistang pagkuwan. Kapiling ang kani-kanilang alipures at tagapayung putiay nag-uunahang ibenta ang bayan.
	Ano ang silbi ng kabayanihan? Ng limos na Laurelat ginto? Ipangangalan sa iyo’y isang kalyeng baku-bako o kaya’y lumuting monumentong ihian ng mga lasenggero.”

Ang pagdakila sa mga mahihirap o anak pawis ay umabot din sataluktok ng panahong rebulusyong pangmasa. Narito angpagpapatunay mula sa ilang saknong ng mga tula ni Federico Licsi Espino na sinulat na niya sa wikang Ingles.
	“Hands that broadcast sweat
	Brandish placards of protest”
	“In the haciendas of Negros
	Sugar cane turns bitter”
	“In the rice granaries of Luzon.
	Hatred hammers sickles”
	“On old newspaper
	Students write the rubrics of dissent”
	“Why should they who roast
	The suckling grow lean on verbiage?”
	“Why should they who plant and grind
	The corn grow thin on gritang homing?”
	Strike! Strike! Strike!
	A dormant hate erupts
	Bundok Buntis! Arayat! Kanlaon!
	
	May mga katipunan din ng mga tula ang naisa aklat nang panahong ito ng aktibismo. Ilan sa mga ito ay ang mga sumusunod:
